void vTaskGuiTimer(void * pvParameters)
{
	lv_lock();
	/*Change the active screen's background color*/
	lv_obj_set_style_bg_color(lv_screen_active(), lv_color_hex(0x003a57), LV_PART_MAIN);

	/*Create a white label, set its text and align it to the center*/
	lv_obj_t * label = lv_label_create(lv_screen_active());
	lv_label_set_text(label, "Hello world");
	lv_obj_set_style_text_color(lv_screen_active(), lv_color_hex(0xffffff), LV_PART_MAIN);
	lv_obj_align(label, LV_ALIGN_CENTER, 0, 0);
	lv_unlock();

	lv_tick_set_cb(xTaskGetTickCount);

	for (;;)
	{
		lv_timer_handler();
		lv_sleep_ms(20);
	}
}


void vTaskUpdateGuiCounter(void * pvParameters)
{
	TickType_t xTickCount;

	lv_lock();
	lv_obj_t * counterLabel = lv_label_create(lv_screen_active());
	lv_label_set_text(counterLabel, "#ff0000 RED TEXT#");
	lv_obj_align(counterLabel, LV_ALIGN_CENTER, 0, 40);
	lv_label_set_recolor(counterLabel, true);
	lv_unlock();

	for (;;)
	{
		xTickCount = xTaskGetTickCount();
		lv_lock();
		lv_label_set_text_fmt(counterLabel, "#ff0000 Count: %d#", (uint32_t)xTickCount);
		lv_unlock();
		lv_sleep_ms(1000);
	}
}


